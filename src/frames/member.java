    private void jButton1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton1ActionPerformed

        JFileChooser fileChooser = new JFileChooser();
        fileChooser.showOpenDialog(null);
        File file = fileChooser.getSelectedFile();
        if (file != null) {
            txt_imagePath.setPreferredSize(new Dimension(txt_imagePath.getWidth(), txt_imagePath.getHeight()));
            txt_imagePath.setText(file.getAbsolutePath());
            txt_imagePath.setEnabled(false);
            try {
                FileInputStream fis = new FileInputStream(file);
                ByteArrayOutputStream byteArray = new ByteArrayOutputStream();
                byte[] buffer = new byte[1024];
                for (int readNum; (readNum = fis.read(buffer)) != -1;) {
                    byteArray.write(buffer, 0, readNum);
                    personImage = byteArray.toByteArray();
                    ImageIcon image = new ImageIcon(personImage);
                    lbl_imageLabel.setIcon(new ImageIcon(resizeImage(image.getImage(), lbl_imageLabel.getWidth(), lbl_imageLabel.getHeight())));
                }

            } catch (Exception ex) {
                Logger.getLogger(member.class.getName()).log(Level.SEVERE, null, ex);
                ex.printStackTrace();
            }
        }
    }//GEN-LAST:event_jButton1ActionPerformed
