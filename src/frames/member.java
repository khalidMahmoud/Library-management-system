    private void tbl_memberDataMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_tbl_memberDataMouseClicked
        // TODO add your handling code here:

        int row = tbl_memberData.getSelectedRow();
        String tableClick = (tbl_memberData.getValueAt(row, 4).toString());
        ResultSet result = db.select("member", new String[]{"*"}, new String[]{"id"}, new String[]{tableClick}, "=", "and");
        try {
            if (result.next()) {
                txt_name.setText(result.getString("name"));
                txt_password.setText(result.getString("password"));
                txt_address.setText(result.getString("address"));
                txt_phone.setText(result.getString("phone"));
                txt_email.setText(result.getString("email"));
                if (result.getString("type").equals("أمين مكتيه")) {
                    tybe_ComboBox.setSelectedItem("أمين مكتي");
                } else {
                    tybe_ComboBox.setSelectedItem("عضو مشترك");
                }
                personImage = result.getBytes("image");
                if (personImage == null) {
                    memberImage = new ImageIcon("deny.jpg");
                } else {
                    memberImage = new ImageIcon(personImage);
                    lbl_imageLabel.setIcon(new ImageIcon(resizeImage(memberImage.getImage(), lbl_imageLabel.getWidth(), lbl_imageLabel.getHeight())));
                }

            }
        } catch (SQLException ex) {
            Logger.getLogger(member.class.getName()).log(Level.SEVERE, null, ex);
        }
    }//GEN-LAST:event_tbl_memberDataMouseClicked

    
    private void btn_deleteActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btn_deleteActionPerformed
        // TODO add your handling code here:
        int row = tbl_memberData.getSelectedRow();
        String tableClick = tbl_memberData.getValueAt(row, 4).toString();
        int result = JOptionPane.showConfirmDialog(null, "هل تريد مسح العضو");
        if (result == JOptionPane.YES_OPTION) {
            try {
                int result2 = db.delete("member", new String[]{"id"}, new String[]{tableClick}, "=", "and");
                if (result2 == -1) {
                    JOptionPane.showMessageDialog(null, "تم المسح");
                } else {
                    JOptionPane.showMessageDialog(null, "لم يتم المسح " + result2);
                }
                emptyFields();
                updateMemberTable();
            } catch (SQLException ex) {
                Logger.getLogger(member.class.getName()).log(Level.SEVERE, null, ex);
            } catch (ClassNotFoundException ex) {
                Logger.getLogger(member.class.getName()).log(Level.SEVERE, null, ex);
            }
        }
    }//GEN-LAST:event_btn_deleteActionPerformed

